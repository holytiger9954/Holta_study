package quiz.quiz06.Emp2;

public class Emp {

	int emp;
	String name;
	String position;
	int salary;
	int boss;
	String bossName;
	
	Emp(int emp, String name, String position, int salary, int boss, String bossName) {
		this.emp = emp;
		this.name = name;
		this.position = position;
		this.salary = salary;
		this.boss = boss;
		this.bossName = bossName;
	}
	
}
